    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        if (cmbpreguntasecreta.getSelectedIndex() == 0 || txtrespuesta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor seleccionar una pregunta y escribir la respuesta de la misma.");
        } else {
            Encriptacion encriptacion = new Encriptacion();
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
            EntityManager em = emf.createEntityManager();
            EntityTransaction et = em.getTransaction();
            try {
                Login login = em.find(Login.class, txtusuario.getText());
                login.setPregunta_Secreta(cmbpreguntasecreta.getSelectedIndex());
                login.setRespuesta(txtrespuesta.getText());
                et.begin();
                em.merge(login);
                et.commit();
                JOptionPane.showMessageDialog(null, "La pregunta ha sido cambiada con satisfacci√≥n.");
                dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
                Logger.getLogger(Cambio_Pregunta_Respuesta.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                em.close();
            }
        }

    }//GEN-LAST:event_btnaceptarActionPerformed


    public void cargar_Estudios() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
        EntityManager em = emf.createEntityManager();
        EntityTransaction et = em.getTransaction();
        try {
            TypedQuery<Estudios> consulta_Estudios = em.createNamedQuery("Estudios.findAll", Estudios.class);
            List<Estudios> lista_Estudios = consulta_Estudios.getResultList();
            for (Estudios a : lista_Estudios) {
                cmbtipoestudio.addItem(a.getTipoEstudio());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
            Logger.getLogger(Datos_Egresados_Estudios_Trabajo.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            em.close();
        }
    }

    public void cargar_Trabajos() {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
        EntityManager em = emf.createEntityManager();
        EntityTransaction et = em.getTransaction();
        try {
            TypedQuery<Trabajos> consulta_Trabajos = em.createNamedQuery("Trabajos.findAll", Trabajos.class);
            List<Trabajos> lista_Trabajos = consulta_Trabajos.getResultList();
            for (Trabajos a : lista_Trabajos) {
                cmbtipotrabajo.addItem(a.getNombreTrabajo());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
            Logger.getLogger(Datos_Egresados_Estudios_Trabajo.class.getName()).log(Level.SEVERE, null, e);
        } finally {
            em.close();
        }
    }


    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:
        if (btnsiestudio.isSelected() && cmbtipoestudio.getSelectedIndex() == 0 || btnsitrabajo.isSelected() && cmbtipotrabajo.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor diligenciar completamente los datos.");
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
            EntityManager em = emf.createEntityManager();
            EntityTransaction et = em.getTransaction();
            try {
                int id_egresado = Integer.parseInt(txtidegresado.getText());
                int codigo_estudio = 0, codigo_trabajo = 0;

                Egresados_Estudios egresado_estudios = em.find(Egresados_Estudios.class, id_egresado);
                if (btnnoestudio.isSelected()) {
                    if (egresado_estudios == null) {
                    } else {
                        et.begin();
                        em.remove(egresado_estudios);
                        et.commit();
                    }
                }

                Egresados_Trabajos egresado_trabajos = em.find(Egresados_Trabajos.class, id_egresado);
                if (btnnotrabajo.isSelected()) {
                    if (egresado_trabajos == null) {
                    } else {
                        et.begin();
                        em.remove(egresado_trabajos);
                        et.commit();
                    }
                }

                if (btnsiestudio.isSelected()) {
                    TypedQuery<Estudios> consulta_Estudios = em.createNamedQuery("consulta_e", Estudios.class);
                    consulta_Estudios.setParameter("nombre", cmbtipoestudio.getSelectedItem().toString());
                    List<Estudios> lista_Estudios = consulta_Estudios.getResultList();
                    for (Estudios a : lista_Estudios) {
                        codigo_estudio = a.getCodigoEstudio();
                    }
                    if (egresado_estudios == null) {
                        egresado_estudios = new Egresados_Estudios();
                        egresado_estudios.setId_Egresado(id_egresado);
                        egresado_estudios.setCodigo_Estudio(codigo_estudio);
                        et.begin();
                        em.persist(egresado_estudios);
                        et.commit();
                    } else {
                        egresado_estudios.setCodigo_Estudio(codigo_estudio);
                        et.begin();
                        em.merge(egresado_estudios);
                        et.commit();
                    }
                }
                if (btnsitrabajo.isSelected()) {
                    TypedQuery<Trabajos> consulta_Trabajos = em.createNamedQuery("consulta_t", Trabajos.class);
                    consulta_Trabajos.setParameter("nombre", cmbtipotrabajo.getSelectedItem().toString());
                    List<Trabajos> lista_Trabajos = consulta_Trabajos.getResultList();
                    for (Trabajos a : lista_Trabajos) {
                        codigo_trabajo = a.getCodigoTrabajo();
                    }

                    if (egresado_trabajos == null) {
                        egresado_trabajos = new Egresados_Trabajos();
                        egresado_trabajos.setId_Egresado(id_egresado);
                        egresado_trabajos.setCodigo_Trabajo(codigo_trabajo);
                        et.begin();
                        em.persist(egresado_trabajos);
                        et.commit();
                    } else {
                        egresado_trabajos.setCodigo_Trabajo(codigo_trabajo);
                        et.begin();
                        em.merge(egresado_trabajos);
                        et.commit();
                    }
                }

                JOptionPane.showMessageDialog(null, "Los datos se han actualizado correctamente.");

                cmbtipoestudio.setEnabled(false);
                cmbtipotrabajo.setEnabled(false);
                btnsiestudio.setEnabled(false);
                btnnoestudio.setEnabled(false);
                btnsitrabajo.setEnabled(false);
                btnnotrabajo.setEnabled(false);

                btnaceptar.setEnabled(false);
                btncancelar.setEnabled(false);

                btnmodificar.setEnabled(true);
                btnnuevaconsulta.setEnabled(true);
                btnmodificar.requestFocus();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
                Logger.getLogger(Datos_Egresados_Estudios_Trabajo.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                em.close();
            }

        }
    }//GEN-LAST:event_btnaceptarActionPerformed

    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
        EntityManager em = emf.createEntityManager();
        EntityTransaction et = em.getTransaction();
        if (txtidegresadoc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor diligenciar el Id del egresado para realizar la consulta");
            txtidegresadoc.requestFocus();
        } else {
            try {
                int id_egresado = Integer.parseInt(txtidegresadoc.getText());
                Egresados egresados = em.find(Egresados.class, id_egresado);
                if (egresados == null) {
                    JOptionPane.showMessageDialog(null, "No hay ningun egresado con el ID " + id_egresado + "\n"
                            + "Para crear un nuevo egresado dirigase al Menu Egresados/Egresado");
                } else {
                    txtidegresado.setText(Integer.toString(egresados.getId_Egresado()));
                    cmbtipodocumento.setSelectedItem(egresados.getTipo_Documento());
                    txtnombre.setText(egresados.getPrimer_Nombre() + " " + egresados.getSegundo_Nombre());
                    txtapellido.setText(egresados.getPrimer_Apellido() + " " + egresados.getSegundo_Apellido());
                    btnmodificar.setEnabled(true);
                    btnnuevaconsulta.setEnabled(true);

                    txtidegresadoc.setText("");
                    txtidegresadoc.setEditable(false);
                    btnconsultar.setEnabled(false);
                    btnmodificar.requestFocus();
                    Egresados_Estudios egresado_estudios = em.find(Egresados_Estudios.class, id_egresado);
                    if (egresado_estudios == null) {
                        btnnoestudio.setSelected(true);
                    } else {
                        btnsiestudio.setSelected(true);
                        Estudios estudios = em.find(Estudios.class, egresado_estudios.getCodigo_Estudio());
                        cmbtipoestudio.setSelectedItem(estudios.getTipoEstudio());
                    }
                    Egresados_Trabajos egresado_trabajos = em.find(Egresados_Trabajos.class, id_egresado);
                    if (egresado_trabajos == null) {
                        btnnotrabajo.setSelected(true);
                    } else {
                        btnsitrabajo.setSelected(true);
                        Trabajos trabajos = em.find(Trabajos.class, egresado_trabajos.getCodigo_Trabajo());
                        cmbtipotrabajo.setSelectedItem(trabajos.getNombreTrabajo());
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
                Logger.getLogger(Datos_Egresados_Estudios_Trabajo.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                em.close();
            }
        }
    }//GEN-LAST:event_btnconsultarActionPerformed


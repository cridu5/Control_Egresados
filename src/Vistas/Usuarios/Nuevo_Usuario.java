    private void btnaceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaceptarActionPerformed
        // TODO add your handling code here:

        String contra1 = new String(pswcontraseña.getPassword());
        String contra2 = new String(pswconfirmacioncontraseña.getPassword());
        if (contra1.equals("") || contra2.equals("") || cmbpreguntasecreta.getSelectedIndex() == 0 || txtrespuesta.getText().equals("") || cmbtipousuario.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor diligenciar todos los datos");
            pswcontraseña.requestFocus();
        } else {
            String contraseña1 = new String(pswcontraseña.getPassword());
            String contraseña2 = new String(pswconfirmacioncontraseña.getPassword());

            if (!contraseña1.equals(contraseña2)) {
                JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.");
                pswcontraseña.setText("");
                pswconfirmacioncontraseña.setText("");
                pswcontraseña.requestFocus();
            } else {
                EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
                EntityManager em = emf.createEntityManager();
                EntityTransaction et = em.getTransaction();
                try {
                    Encriptacion encriptar = new Encriptacion();
                    String usuario = txtusuario.getText();

                    String contraseña;
                    contraseña = encriptar.encriptar(contra1);

                    Login login = new Login();
                    login.setUsuario(usuario);
                    login.setContraseña(contraseña);
                    login.setPregunta_Secreta(cmbpreguntasecreta.getSelectedIndex());
                    login.setRespuesta(txtrespuesta.getText());
                    login.setTipo_Usuario(cmbtipousuario.getSelectedItem().toString());

                    et.begin();
                    em.persist(login);
                    et.commit();
                    JOptionPane.showMessageDialog(null, "El Usuario ha sido creado con satisfacción.");
                    dispose();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error: " + e);
                    Logger.getLogger(Nuevo_Usuario.class.getName()).log(Level.SEVERE, null, e);
                } finally {
                    em.close();
                }
            }
        }
    }//GEN-LAST:event_btnaceptarActionPerformed


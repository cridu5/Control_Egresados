    private void btnconsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnconsultarActionPerformed
        // TODO add your handling code here:
        if (txtusuarioc.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Por favor diligenciar el usuario.");
            txtusuarioc.requestFocus();
        } else {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("Control_Egresados_JPAPU");
            EntityManager em = emf.createEntityManager();
            EntityTransaction et = em.getTransaction();
            try {
                Login login = em.find(Login.class, txtusuarioc.getText());
                if (login == null) {
                    txtusuario.setText(txtusuarioc.getText());
                    txtusuarioc.setText("");
                    txtusuarioc.setEditable(false);
                    btnconsultar.setEnabled(false);
                    pswcontraseña.setEditable(true);
                    pswconfirmacioncontraseña.setEditable(true);
                    cmbpreguntasecreta.setEnabled(true);
                    txtrespuesta.setEditable(true);
                    cmbtipousuario.setEnabled(true);
                    btnaceptar.setEnabled(true);
                    btncancelar.setEnabled(true);
                    pswcontraseña.requestFocus();
                } else {
                    JOptionPane.showMessageDialog(null, "El usuario " + txtusuarioc.getText() + " ya existe en la base de datos.");
                    txtusuarioc.setText("");
                    txtusuarioc.requestFocus();
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: " + e);
                Logger.getLogger(Nuevo_Usuario.class.getName()).log(Level.SEVERE, null, e);
            } finally {
                em.close();
            }
        }
    }//GEN-LAST:event_btnconsultarActionPerformed

